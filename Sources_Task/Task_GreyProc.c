#include "app_cfg.h"

void Task_GreyProc (void  *p_arg)
{
    OS_ERR  os_err;
    
    while(TRUE)
    {
        /* 每0.1s采集处理一次 灰度信息 */
        OSTimeDlyHMSM(0u, 0u, 0u, 100,
                      OS_OPT_TIME_HMSM_STRICT | OS_OPT_TIME_PERIODIC,/* 周期模式 */
                      &os_err);
        
        
        TRACE_DBG(">>DBG:       灰度处理\r\n");
        
        BSP_Led2Toggle();
    }
}